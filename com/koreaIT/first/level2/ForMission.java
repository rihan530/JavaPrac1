package com.koreaIT.first.level2;

public class ForMission {

	public static void main(String[] args) {
		
		int a = 1;
		
		for(int i=0;i<5;i++) {
			
			System.out.println(i+1);
		}
		
		for(int i=0;i<5;i++) {
			System.out.println(a);
			a++;
		}
		
		for(int i=5;i<10;i++) {
			System.out.println(i-4);
		}
		
		for(int i =17; i<22; i++) {
			System.out.println(i-15);
		}

	}

}
